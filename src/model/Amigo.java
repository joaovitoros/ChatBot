package model;

public class Amigo {
	private String nome;
	private int contHumor;
	private String apelidoIA;
	
	public String getApelidoIA() {
		return apelidoIA;
	}
	public void setApelidoIA(String apelidoIA) {
		this.apelidoIA = apelidoIA;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getContHumor() {
		return contHumor;
	}
	public void setContHumor(int contHumor) {
		this.contHumor = contHumor;
	}
}
